// num1 이라는 이름을 가진 저장소를 생성하고,
// 그 저장소에 100을 저장시켜라.
let num1 = 100;
let num2 = 400;

// console.log(값) 혹은 console.log(변수명)
// 은 지정된 값 혹은 지정된 변수에 저장된 값을
// 화면에 출력시키는 수행문이다.
console.log(num1);
console.log(num2);

// num1에 저장된 값과 num2에 저장된 값을 더해서
// num3를 만들어서 저장시켜라
let num3 = num1 + num2;
// num3에 저장된 값을 출력해라
console.log(num3);
